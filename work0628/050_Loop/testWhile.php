<?php
$iSum = 0;
$i = 1;
while ($i <= 10)  //設定次數 1~10 = 10次
{
	$iSum += $i;  //iSum = isum + i
	$i += 1; // i++
}
echo $iSum; // 0+1+2+.....+10 = 550

echo "<hr>";

$iSum = 0;
$i = 0;
while ($i < 10) //  0~9   >> 10次
{
	$i++;  
	$iSum += $i;	// 0+1+2+3+.....+10= 550
}
echo $iSum  // 0+1+2+3+.....+10= 550
?>